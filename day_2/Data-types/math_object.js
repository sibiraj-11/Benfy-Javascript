let PI = Math.PI

console.log(Math.round(PI));

// to round values to the nearest number
console.log(Math.round(8.098)); 

// rounding down
console.log(Math.floor(PI));

// rounding up
console.log(Math.ceil(PI));

// returns the minimum value
console.log(Math.min(-9,8,4,4,3,2,0,-30));

// returns the maximum value
console.log(Math.max(100, 300, 10**4));

// creates random number between 0 to 0.999999
console.log(Math.random());

// creates random number between 0 and 100
console.log(Math.floor(Math.random()*100));

// absolute values
console.log(Math.abs(-20));

// square roots
console.log(Math.sqrt(10));

// power
console.log(Math.pow(2,5));


// Logarithm
// Returns the natural logarithm with base E of x, Math.log(x)
console.log(Math.log(2))     
console.log(Math.log(10))      

// Returns the natural logarithm of 2 and 10 respectively
console.log(Math.LN2)          
console.log(Math.LN10)        

// Trigonometry
Math.sin(0)
Math.sin(60)

Math.cos(0)
